function applyLanguage(lang) {
  const dictionary = translations[lang] || translations.es;

  document.documentElement.lang = lang;
  localStorage.setItem("portfolioLanguage", lang);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll('a[href^="cv.html"]').forEach((link) => {
    link.href = `cv.html?lang=${lang}`;
  });

  if (document.body.classList.contains("cv-page")) {
    document.title = lang === "es"
      ? "CV Español | Argenis David Cerrato Amador"
      : "CV English | Argenis David Cerrato Amador";
  }

  document.querySelectorAll(".lang-btn").forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
}

function setupRevealAnimations() {
  const elements = document.querySelectorAll(".reveal");

  if (!("IntersectionObserver" in window)) {
    elements.forEach((element) => element.classList.add("visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.12 }
  );

  elements.forEach((element) => observer.observe(element));
}

function setupNavbarBehavior() {
  const navLinks = document.querySelectorAll(".navbar .nav-link[href^='#']");
  const sections = [...navLinks]
    .map((link) => document.querySelector(link.getAttribute("href")))
    .filter(Boolean)
    .sort((a, b) => a.offsetTop - b.offsetTop);
  const collapsibleNav = document.getElementById("mainNav");
  const toggler = document.querySelector(".navbar-toggler");
  const setActiveLink = (sectionId) => {
    navLinks.forEach((link) => {
      link.classList.toggle("active", link.getAttribute("href") === `#${sectionId}`);
    });
  };
  const updateActiveLink = () => {
    if (sections.length === 0) {
      return;
    }

    const headerOffset = document.querySelector(".site-header")?.offsetHeight || 0;
    const scrollPosition = window.scrollY + headerOffset + 72;
    const pageBottom = window.innerHeight + window.scrollY >= document.documentElement.scrollHeight - 12;
    let currentSection = sections[0];

    if (pageBottom) {
      setActiveLink(sections[sections.length - 1].id);
      return;
    }

    sections.forEach((section) => {
      if (section.offsetTop <= scrollPosition) {
        currentSection = section;
      }
    });

    setActiveLink(currentSection.id);
  };

  if (!window.bootstrap && toggler && collapsibleNav) {
    toggler.addEventListener("click", () => {
      const isOpen = collapsibleNav.classList.toggle("show");
      toggler.setAttribute("aria-expanded", String(isOpen));
    });
  }

  navLinks.forEach((link) => {
    link.addEventListener("click", () => {
      if (!collapsibleNav?.classList.contains("show")) {
        return;
      }

      if (window.bootstrap) {
        window.bootstrap.Collapse.getOrCreateInstance(collapsibleNav).hide();
      } else {
        collapsibleNav.classList.remove("show");
        toggler?.setAttribute("aria-expanded", "false");
      }
    });
  });

  updateActiveLink();
  window.addEventListener("scroll", updateActiveLink, { passive: true });
  window.addEventListener("resize", updateActiveLink);
}

function setupProtectedContactLinks() {
  const contactParts = {
    email: ["david", "amador0999", "gmail", "com"],
    phone: ["+506", "8684", "7372"]
  };

  const email = `${contactParts.email[0]}${contactParts.email[1]}@${contactParts.email[2]}.${contactParts.email[3]}`;
  const phoneHref = `tel:${contactParts.phone.join("")}`;
  const phoneLabel = `${contactParts.phone[1]}-${contactParts.phone[2]}`;

  document.querySelectorAll('[data-contact="email"]').forEach((link) => {
    link.href = `mailto:${email}`;
    link.setAttribute("aria-label", `Enviar correo a ${email}`);
  });

  document.querySelectorAll('[data-contact="phone"]').forEach((link) => {
    link.href = phoneHref;
    link.setAttribute("aria-label", `Llamar al ${phoneLabel}`);
  });

  document.querySelectorAll('[data-contact-label="email"]').forEach((element) => {
    element.textContent = email;
  });

  document.querySelectorAll('[data-contact-label="phone"]').forEach((element) => {
    element.textContent = phoneLabel;
  });
}

document.addEventListener("DOMContentLoaded", () => {
  const requestedLanguage = new URLSearchParams(window.location.search).get("lang");
  const savedLanguage = ["es", "en"].includes(requestedLanguage)
    ? requestedLanguage
    : localStorage.getItem("portfolioLanguage") || "es";
  const currentYear = document.getElementById("currentYear");

  if (currentYear) {
    currentYear.textContent = new Date().getFullYear();
  }

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.addEventListener("click", () => applyLanguage(button.dataset.lang));
  });

  applyLanguage(savedLanguage);
  setupProtectedContactLinks();
  setupRevealAnimations();
  setupNavbarBehavior();
});
